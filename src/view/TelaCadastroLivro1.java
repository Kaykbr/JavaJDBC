    private void SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarActionPerformed
          if (!this.podeEditar) {
            JOptionPane.showMessageDialog(this, "Clique para adicionar um livro");
            return;
          }
          
        int ISBN = Integer.parseInt(ISBNText.getText());
        String titulo = TituloText.getText();
        String autor = AutorText.getText();
        String categoria = CategoriaText.getText();
        int paginas = Integer.parseInt(PaginasText.getText());
        double preco = Double.parseDouble(PrecoText.getText());
        this.preencherLivro(lSelecionado, ISBN, titulo, autor, categoria, paginas, preco);
        
        if (lSelecionado != null && !( titulo.equals("") || autor.equals("") || categoria.equals("") )) {
            if (lSelecionado.getId() != 0) {
                // atualizar
                lController.salvar(LtabelaModelo, lSelecionado, false);
            } else {
                // criar novo
                lController.salvar(LtabelaModelo, lSelecionado, true);
            }
            
            
        } else {
            JOptionPane.showMessageDialog(this, "Preencha todos os camposlistados.");
        }
    }//GEN-LAST:event_SalvarActionPerformed

    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed
       if (lSelecionado == null) {
            JOptionPane.showMessageDialog(this, "Livro inexistente.\nTente selecionar um vendedor da tabela ." );
        } else {
            lController.excluir(LtabelaModelo, lSelecionado);
        }
        limparCampos();
    }//GEN-LAST:event_ExcluirActionPerformed

    private void ProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ProcurarActionPerformed
         Object[] possibilities = {"Todos", "Pelo ID", "Pelo ISBN"};
        String escolha = (String) JOptionPane.showInputDialog(
                this,
                "Escolha o tipo de busca\n"
                + "que deseja efetuar",
                "Buscar Livro",
                JOptionPane.QUESTION_MESSAGE,
                null,
                possibilities,
                possibilities[0]);
        
        escolha = escolha == null ? "" : escolha;

        switch (escolha) {
            case "Pelo Código":
                int id = Integer.parseInt(JOptionPane.showInputDialog(this, "Código do livro: "));
                lController.listarPorId(LtabelaModelo, id);
                break;
            case "Pelo ISBN":
                String ISBN = (JOptionPane.showInputDialog(this, "Insira o isbn: "));
                lController.listarPorISBN(LtabelaModelo, ISBN);
                break;
            default:
                lController.listarTodos(LtabelaModelo);
        }
    }//GEN-LAST:event_ProcurarActionPerformed

