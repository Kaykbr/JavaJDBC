    public void CriarLTabelaModelo() {

        LtabelaModelo = new javax.swing.table.DefaultTableModel(
                new Object[][]{
                    {null, null, null, null, null, null}
                },
                new String[]{
                    "ISBN", "Titulo", "Autor", "Categoria", "Paginas", "Preço"
                }
        ) {
            Class[] types = new Class[]{
                java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class
            };

            boolean[] canEdit = new boolean[]{
                 false, false, false, false, false, false
            };

            public Class getColumnClass(int columnIndex) {
                return types[columnIndex];
            }

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }

        };
    }
        public void preencherSelecionado(ListSelectionEvent e) {
        int linha = TabelaDeLivros.getSelectedRow();
        try {
            
            int id = Integer.parseInt(LtabelaModelo.getValueAt(linha, 0).toString());
            int ISBN = Integer.parseInt(LtabelaModelo.getValueAt(linha, 1).toString());
            String titulo = LtabelaModelo.getValueAt(linha, 2).toString();
            String autor = LtabelaModelo.getValueAt(linha, 3).toString();
            String categoria = LtabelaModelo.getValueAt(linha, 4).toString();
            int paginas = Integer.parseInt(LtabelaModelo.getValueAt(linha, 5).toString());
            Double preco = Double.parseDouble(LtabelaModelo.getValueAt(linha, 6).toString());

            this.preencherLivro(lSelecionado, ISBN, titulo, autor, categoria, paginas, preco);

            this.preencherCampos();
            this.habilitarCamposEdicao();
        } catch (Exception erro) {
            this.limparCampos();
        }
    }
        public void preencherLivro(Livro l, int ISBN, String titulo, String autor, String categoria, int paginas, Double preco) {
        if (ISBN != 0 && titulo != null && autor != null && categoria != null && paginas != 0 && preco != 0) {
            l.setISBN(ISBN);
            l.setTitulo(titulo);
            l.setAutor(autor);
            l.setCategoria(categoria);
            l.setPaginas(paginas);
            l.setPreco(preco);
            
        } else {
            this.limparCampos();
        }
    }

